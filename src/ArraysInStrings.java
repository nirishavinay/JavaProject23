package src;

public class ArraysInStrings
{
    public static void main(String[] args)
{
    String[] flowers = {"Rose","Jasmine","lilly","Sunflower"};
    System.out.println("The Flowers in Arrays are: ");
    for(int i = 0; i < flowers.length; i++)
    {
        System.out.println(flowers[i]);
    }
}
}
