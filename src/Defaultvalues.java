package src;

public class Defaultvalues
{
//declare the variables of primitive type

    static byte bvalue;
    static short svalue;
    static int ivalue;
    static float fvalue;
    static long lvalue;
    static double dvalue;
    static char cvalue;
    static boolean b1value;

    public static void main(String[] args) {

//print the default values of primitive datatypes

        System.out.println(" Default value of byte = " + bvalue);
        System.out.println(" Default value of short = " + svalue);
        System.out.println(" Default value of int = " + ivalue);
        System.out.println(" Default value of float = " + fvalue);
        System.out.println(" Default value of long = " + lvalue);
        System.out.println(" Default value of double = " + dvalue);
        System.out.println(" Default value of char = " + cvalue);
        System.out.println(" Default value of boolean = " + b1value);

    }
}